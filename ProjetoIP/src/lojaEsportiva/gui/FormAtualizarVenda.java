    private boolean validaCampos() {
        if (!((txtID.getText().length() > 0) && (txtID.getText() != null))) {
            JOptionPane.showMessageDialog(null, "O campo ID deve ser informado!");
            txtID.requestFocus();
            return false;
        }
        if (!((txtIdProduto.getText().length() > 0) && (txtIdProduto.getText() != null))) {
            JOptionPane.showMessageDialog(null, "O campo ID Produto deve ser informado!");
            txtIdProduto.requestFocus();
            return false;
        }
        if (!((txtCpfCliente.getText().length() > 0) && (txtCpfCliente.getText() != null))) {
            JOptionPane.showMessageDialog(null, "O campo CPF Cliente deve ser informado!");
            txtCpfCliente.requestFocus();
            return false;
        }
        if (!((txtIdFuncionario.getText().length() > 0) && (txtIdFuncionario.getText() != null))) {
            JOptionPane.showMessageDialog(null, "O campo ID Funcionario deve ser informado!");
            txtIdFuncionario.requestFocus();
            return false;
        }
        return true;
    }
    
    private void limparCampos() {
        txtID.setText("");
        txtIdProduto.setText("");
        txtCpfCliente.setText("");
        txtIdFuncionario.setText("");
    }
    
