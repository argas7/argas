    private boolean validaCampos() {
        if (!((txtID.getText().length() > 0) && (txtID.getText() != null))) {
            JOptionPane.showMessageDialog(null, "O campo ID deve ser informado!");
            txtID.requestFocus();
            return false;
        }
        return true;
    }
    
