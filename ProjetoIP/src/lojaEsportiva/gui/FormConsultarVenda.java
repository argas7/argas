    private void btProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarActionPerformed
        // TODO add your handling code here:
        if (validaCampos()) {
            try{
                Venda venda = FormPrincipal.fachada.procurarVenda(txtID.getText());
                txtCPFCliente.setText(venda.getCliente().getId());
                txtNomeCliente.setText(venda.getCliente().getNome());
                txtContatoCliente.setText(venda.getCliente().getContato());
                txtIDProduto.setText(venda.getProduto().getIdentificador());
                txtTipoProduto.setText(venda.getProduto().getTipo());
                txtMarca.setText(venda.getProduto().getMarca());
                txtIDFuncionario.setText(venda.getFuncionario().getIdFuncionario());
                txtNomeFuncionario.setText(venda.getFuncionario().getNome());
                txtContatoFuncionario.setText(venda.getFuncionario().getContato());
            } catch (VendaNaoEncontradaException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
                limparCampos();
            }
        }
    }//GEN-LAST:event_btProcurarActionPerformed

