    private void btProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarActionPerformed
        // TODO add your handling code here:
        if (validaCampos()) {
            try{
                Produto produto = FormPrincipal.fachada.procurarProduto(txtIdProduto.getText());
                txtTipo.setText(produto.getTipo());
                txtMarca.setText(produto.getMarca());
                txtModelo.setText(produto.getModelo());
                txtTamanho.setText(produto.getTamanho());
                txtPreco.setText(String.valueOf(produto.getPreco()));
            } catch (ProdutoNaoEncontradoException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
                limparCampos();
            }
        }
    }//GEN-LAST:event_btProcurarActionPerformed

