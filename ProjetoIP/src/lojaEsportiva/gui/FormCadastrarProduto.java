    private void btCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarProdutoActionPerformed
        // TODO add your handling code here:
        if (validaCampos()) {
            try{
                Produto produto = new Produto(txtTamanho.getText(), txtModelo.getText(), txtMarca.getText(), txtID.getText(), txtTipo.getText(), Double.parseDouble(txtPreco.getText()));
                FormPrincipal.fachada.cadastrarProduto(produto);
                JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
                limparCampos();
            } catch (ProdutoJaCadastradoException e){
                JOptionPane.showMessageDialog(null, e.getMessage());
                limparCampos();
            }    
        }
    }//GEN-LAST:event_btCadastrarProdutoActionPerformed

